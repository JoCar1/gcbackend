    {
        $categoria = Categoria::find($id);
        $request->validate([
            'nombre' => 'required|string|max:255'
        ]);
        $requestData = $request->all();
        $categoria->update($requestData);
        $mensaje = "Actualizado correctamente";
        return response()->json(['mensaje'=>$mensaje], 200);
    }

